#include <bits/stdc++.h>

using namespace std;

typedef unsigned long long ull;
typedef signed long long ll;
typedef unsigned int uint;
typedef unsigned char uchar;

template <typename T>
void print_array(const T v[], uint n);
template <typename T>
void print_vector(const vector<T> &v, uint n = 0);
template <typename T>
void read_array(T v[], uint n);
template <typename T>
void read_array_and_size(T v[], uint &n);
template <typename T>
void read_vector(vector<T> &v, uint n = 0);
template <typename T>
void read_vector_and_size(vector<T> &v, uint &n);
void prep();

const uint nmax = 1e5;

int main()
{
  prep();

  int t;
  cin >> t;
  while (t--)
  {
    ull n, a, b;
    cin >> n >> a >> b;

    if (n == 1 || b == 1 || (a == 1 && (n - 1) % b == 0))
    {
      cout << "Yes\n";
      continue;
    }

    ull r = 1;
    bool sol = false;

    for (ull i = 0ULL; r <= n && i <= 32; i++)
    {
      if ((n - r) % b == 0)
      {
        sol = true;
        break;
      }

      r *= a;
    }

    cout << (sol ? "Yes" : "No") << "\n";
  }

  return 0;
}

