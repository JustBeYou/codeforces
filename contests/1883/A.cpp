template <typename T>
void print_array(const T v[], uint n);
template <typename T>
void print_vector(const vector<T> &v, uint n = 0);
template <typename T>
void read_array(T v[], uint n);
template <typename T>
void read_array_and_size(T v[], uint &n);
template <typename T>
void read_vector(vector<T> &v, uint n = 0);
template <typename T>
void read_vector_and_size(vector<T> &v, uint &n);
void prep();

const uint nmax = 1e5;

int main()
{
  prep();

  int t;
  string s;
  string pad("1234567890");
  cin >> t;
  while (t--)
  {
    cin >> s;

    int cur = 0;
    int sol = 0;
    for (auto ch : s)
    {
      auto pos = pad.find(ch);
      assert(pos != string::npos);
      sol += abs(cur - (int)pos) + 1;
      cur = pos;
    }
    cout << sol << "\n";
  }

  return 0;
}

