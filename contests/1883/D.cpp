void read_vector_and_size(vector<T> &v, uint &n);
void prep();

using puu = pair<uint, uint>;
using mst = multiset<puu>;

int main()
{
  prep();

  uint q;
  cin >> q;

  mst left_first;
  mst right_first;

  while (q--)
  {
    char op;
    uint l, r;
    cin >> op >> l >> r;
    // cout << op << " " << l << " " << r << endl;

    if (op == '+')
    {
      left_first.insert({l, r});
      right_first.insert({r, l});
    }
    else
    {
      left_first.erase(left_first.find({l, r}));
      right_first.erase(right_first.find({r, l}));
    }

    bool good = left_first.size() > 1 && (prev(left_first.end())->first > right_first.begin()->first);

    cout << (good ? "YES\n" : "NO\n");
  }

  return 0;
}

