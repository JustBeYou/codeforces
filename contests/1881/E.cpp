void prep();

const uint nmax = 2e5 + 5;
const uint inf = 2 * nmax;

uint dp[nmax];
uint a[nmax];

int main()
{
  prep();

  int t;
  cin >> t;
  while (t--)
  {
    uint n;
    cin >> n;
    for (uint i = 1; i <= n; i++)
    {
      cin >> a[i];
    }

    memset(dp, inf, sizeof(uint) * (n + 1));
    for (uint i = 1; i <= n; i++)
    {
      dp[i] = min(dp[i], dp[i - 1] + 1);

      uint j = i + a[i];
      if (j <= n)
      {
        dp[j] = min(dp[j], min(dp[i - 1], i - 1));
      }
    }

    // for (uint i = 1; i <= n; i++)
    // {
    //   cout << dp[i] << " ";
    // }
    // cout << endl;

    cout << min(dp[n], n) << "\n";
  }

  return 0;
}

