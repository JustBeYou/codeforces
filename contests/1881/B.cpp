void read_vector_and_size(vector<T> &v, uint &n);
void prep();

const uint nmax = 1e5;

int main()
{
  prep();

  int t;
  cin >> t;
  while (t--)
  {
    vector<uint> v;
    read_vector(v, 3);

    sort(v.begin(), v.end());
    for (uint i = 0; i < 3; i++)
    {
      if (v[0] < v[v.size() - 1])
      {
        v[v.size() - 1] -= v[0];
        v.push_back(v[0]);
        sort(v.begin(), v.end());
      }
    }

    bool good = true;
    for (uint i = 0; i < v.size() - 1; i++)
    {
      if (v[i] != v[i + 1])
        good = false;
    }

    cout << (good ? "YES" : "NO") << "\n";
  }

  return 0;
}

