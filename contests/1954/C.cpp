void prep();

const uint nmax = 1e5;

int main()
{
  prep();

  int t;
  cin >> t;
  while (t--)
  {
    string x, y;
    cin >> x >> y;

    uint n = x.size();
    uint i = 0;
    while (i < n && x[i] == y[i])
    {
      i++;
    }

    if (i >= n)
    {
      cout << x << "\n"
           << y << "\n";
      continue;
    }

    uint j = i + 1;
    while (j < n)
    {
      if (x[i] > y[i] && x[j] > y[j])
      {
        swap(x[j], y[j]);
      }

      if (x[i] < y[i] && x[j] < y[j])
      {
        swap(x[j], y[j]);
      }

      j++;
    }

    cout << x << "\n"
         << y << "\n";
  }

  return 0;
}

